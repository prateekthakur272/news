class Article{
  final String? title;
  final String? description;
  final String? source;
  final String? author;
  final String? content;
  final String? articleUrl;
  final String? imageUrl;
  final DateTime? dateTime;

  Article({required this.title, required this.description, required this.source, required this.author, required this.content, required this.articleUrl, required this.imageUrl, required this.dateTime});
}